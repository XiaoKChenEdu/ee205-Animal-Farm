bool classAnimal::validate() const noexcept {

    assert( classNode::validate() ) ;
    assert( !getKingdom().empty() ) ;
    assert( validateClassification( getClassification() ) ) ;
    assert( validateSpecies( getSpecies() ) ) ;
    assert( weight.validate() ) ;

    return true ;

}
